function getLocaleCopy(copyMap, locale = 'en') {
  return copyMap[locale] || copyMap.en
}

export function getGuidesExperienceCopy(locale = 'en') {
  return getLocaleCopy(EXPERIENCE_SIDEBAR_COPY, locale)
}

export function getAboutSidebarCta(locale = 'en') {
  const copy = getGuidesExperienceCopy(locale)

  return {
    title: copy.aboutTitle,
    body: copy.guidesBody,
    button: copy.aboutButton,
    href: copy.aboutHref,
  }
}

export function getGolfCoursesPromoCopy(locale = 'en') {
  return getLocaleCopy(GOLF_COURSES_PROMO_COPY, locale)
}
